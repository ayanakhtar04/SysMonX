const FONT = 'Times New Roman';
const FONT_BODY = 24;       // 12pt
const FONT_SECTION = 26;    // 13pt
const FONT_CHAPTER = 28;    // 14pt
const LINE_SPACING = { line: 360, lineRule: 'auto' }; // 1.5 line spacing
const PARA_SPACING = { before: 0, after: 180 };       // 9pt after paragraph

// Page margins per spec: left 1.25", right 0.75", top 1", bottom 1.5"
const PAGE_MARGINS = { top: 1440, right: 1080, bottom: 2160, left: 1800 };

const cellBorder = { style: BorderStyle.SINGLE, size: 1, color: '888888' };
const cellBorders = { top: cellBorder, bottom: cellBorder, left: cellBorder, right: cellBorder };

function body(text, opts = {}) {
  return new Paragraph({
    spacing: { ...LINE_SPACING, ...PARA_SPACING },
    alignment: AlignmentType.JUSTIFIED,
    children: [new TextRun({ text, font: FONT, size: FONT_BODY, ...opts })],
  });
}

function bodyRuns(runs, opts = {}) {
  return new Paragraph({
    spacing: { ...LINE_SPACING, ...PARA_SPACING },
    alignment: AlignmentType.JUSTIFIED,
    ...opts,
    children: runs,
  });
}

function chapterHeader(chapterNum, title) {
  return [
    new Paragraph({
      alignment: AlignmentType.RIGHT,
      spacing: { before: 0, after: 120 },
      children: [new TextRun({ text: `Chapter - ${chapterNum}`, font: FONT, size: FONT_BODY, bold: false })],
    }),
    new Paragraph({
      heading: HeadingLevel.HEADING_1,
      alignment: AlignmentType.CENTER,
      spacing: { before: 120, after: 360 },
      children: [new TextRun({ text: title.toUpperCase(), font: FONT, size: FONT_CHAPTER, bold: true })],
    }),
  ];
}

function sectionHead(num, title) {
  return new Paragraph({
    heading: HeadingLevel.HEADING_2,
    spacing: { before: 240, after: 120 },
    children: [new TextRun({ text: `${num}  ${title}`, font: FONT, size: FONT_SECTION, bold: true })],
  });
}

function subHead(num, title) {
  return new Paragraph({
    heading: HeadingLevel.HEADING_3,
    spacing: { before: 180, after: 80 },
    children: [new TextRun({ text: `${num}  ${title}`, font: FONT, size: FONT_BODY, bold: true })],
  });
}

function bullet(text) {
  return new Paragraph({
    numbering: { reference: 'bullets', level: 0 },
    spacing: { line: 360, before: 0, after: 80 },
    children: [new TextRun({ text, font: FONT, size: FONT_BODY })],
  });
}

function numbered(text) {
  return new Paragraph({
    numbering: { reference: 'numbers', level: 0 },
    spacing: { line: 360, before: 0, after: 80 },
    children: [new TextRun({ text, font: FONT, size: FONT_BODY })],
  });
}

function codePara(text) {
  return new Paragraph({
    spacing: { before: 80, after: 80, line: 276, lineRule: 'auto' },
    shading: { fill: 'F2F2F2', type: ShadingType.CLEAR },
    indent: { left: 720 },
    children: [new TextRun({ text, font: 'Courier New', size: 20 })],
  });
}

function codeBlock(lines) {
  return lines.map(l => codePara(l));
}

function imagePlaceholder(desc) {
  return new Paragraph({
    spacing: { before: 200, after: 200 },
    alignment: AlignmentType.CENTER,
    border: {
      top: { style: BorderStyle.SINGLE, size: 4, color: '2E75B6' },
      bottom: { style: BorderStyle.SINGLE, size: 4, color: '2E75B6' },
      left: { style: BorderStyle.SINGLE, size: 4, color: '2E75B6' },
      right: { style: BorderStyle.SINGLE, size: 4, color: '2E75B6' },
    },
    children: [new TextRun({ text: `[IMAGE PLACEHOLDER: ${desc}]`, font: FONT, size: 20, italics: true, color: '2E75B6' })],
  });
}

function tableRow(cells, isHeader = false) {
  return new TableRow({
    children: cells.map(c => new TableCell({
      borders: cellBorders,
      shading: isHeader ? { fill: '2E75B6', type: ShadingType.CLEAR } : { fill: 'FFFFFF', type: ShadingType.CLEAR },
      margins: { top: 80, bottom: 80, left: 120, right: 120 },
      verticalAlign: VerticalAlign.CENTER,
      children: [new Paragraph({
        alignment: AlignmentType.CENTER,
        children: [new TextRun({ text: c, font: FONT, size: 20, bold: isHeader, color: isHeader ? 'FFFFFF' : '000000' })],
      })],
    })),
  });
}

function pageBreak() {
  return new Paragraph({ children: [new PageBreak()] });
}

function emptyPara() {
  return new Paragraph({ children: [new TextRun('')] });
}

function centeredBold(text, size = FONT_BODY) {
  return new Paragraph({
    alignment: AlignmentType.CENTER,
    spacing: { before: 120, after: 120 },
    children: [new TextRun({ text, font: FONT, size, bold: true })],
  });
}

function centered(text, size = FONT_BODY, italic = false) {
  return new Paragraph({
    alignment: AlignmentType.CENTER,
    spacing: { before: 60, after: 60 },
    children: [new TextRun({ text, font: FONT, size, italics: italic })],
  });
}

